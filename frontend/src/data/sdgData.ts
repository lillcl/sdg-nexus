const T = (s: number) =>
  s >= 75
    ? "On Track"
    : s >= 60
      ? "Moderately Improving"
      : s >= 45
        ? "Stagnating"
        : s >= 35
          ? "Moderately Declining"
          : "Declining";

RAW.forEach(([iso3, iso2, name, region, subregion, popM, overall, gs], idx) => {
  const scores: Record<string, number> = {};
  const trends: Record<string, string> = {};
  gs.forEach((s, i) => {
    scores[String(i + 1)] = s;
    trends[String(i + 1)] = T(s);
  });
  EMBEDDED_SDG_DATA[iso3] = {
    iso3,
    iso2,
    name,
    region,
    subregion,
    income_group:
      overall >= 75
        ? "High Income"
        : overall >= 65
          ? "Upper Middle Income"
          : overall >= 55
            ? "Lower Middle Income"
            : "Low Income",
    population: Math.round(popM * 1_000_000),
    overall_score: overall,
    rank: idx + 1,
    scores,
    trends,
  };
});

const sorted = Object.values(EMBEDDED_SDG_DATA).sort(
  (a, b) => b.overall_score - a.overall_score,
);
sorted.forEach((c, i) => {
  c.rank = i + 1;
});

